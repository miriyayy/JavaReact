package oopIntro;

public class Category {
	private int id;
	private String name;
	
	public Category() {
		
	}
	
	public Category(int id, String name) {
		
		this.id = id;
		this.name = name;
	}
	public int getId() {
		return this.id;
	}
	public void setId(int id) {
		this.id=id;
	}
	public String getNmae() {
		return this.name;
	}
	public void setName() {
		this.name=name;
	}
}
